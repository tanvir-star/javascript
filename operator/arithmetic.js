// Addition
let sum = 5 + 3;
console.log(sum); // Output: 8

// Subtraction
let difference = 10 - 4;
console.log(difference); // Output: 6

// Multiplication
let product = 6 * 7;
console.log(product); // Output: 42

// Division
let quotient = 20 / 5;
console.log(quotient); // Output: 4

// Modulus (Remainder)
let remainder = 3 % 2;
console.log(remainder); // Output: 1

// Exponentiation
let power = 2 ** 3; // 2 raised to the power of 3
console.log(power); // Output: 8
